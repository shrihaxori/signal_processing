fs = 1000;
f0 = 60;
b0 = 1;
b1 = -2*cos(2*pi*f0/fs);
b2 = 1;
r = 0.999;
a0 = 1;
a1 = -2*r*cos(2*pi*f0/fs);
a2 = r*r;
b = [b2, b1, b0];
a = [a2, a1, a0];
[h,w] = freqz(b, a);
hf = abs(h);
figure(1);
plot(w*fs/(2*pi), hf);
title('Magnitude Response');
xlabel('Frequency in Hz');
figure(2);
zplane(b,a); legend('zero','plot');
